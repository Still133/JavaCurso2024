package cursoJava2024;

public class IfConciso {

    public static void main(String[] args) {
        // Declarando variável:
        int numero = 10;

        // Uso de if ternário:
        String resultado = (numero >= 10) ? "Valor maior/igual que 10" : "Valor MENOR que 10";
        
        // Imprimindo o resultado
        System.out.println(resultado);
    }
}
